    public static void tslRecipeType(CTNHLangProvider provider, String key, String cn) {
        tsl(provider, key, cn, convertToTitleCase(key));
    }
    public static String convertToTitleCase(String key) {
        String name = key;
        if (key.contains(".")) {
            name = key.substring(key.lastIndexOf('.') + 1);
        }

        String[] words = name.replace('_', ' ').split(" ");

        StringBuilder result = new StringBuilder();
        for (int i = 0; i < words.length; i++) {
            if (!words[i].isEmpty()) {
                if (i > 0) {
                    result.append(" ");
                }
                result.append(Character.toUpperCase(words[i].charAt(0)))
                        .append(words[i].substring(1).toLowerCase());
            }
        }

        return result.toString();
    }
