    private static void translateMaterial(CTNHLangProvider provider, Material material, String cn) {
        if (material == null) return;
        var enName = FormattingUtil.toEnglishName(material.getName());
        provider.addMaterial(material, enName, cn);
    }

    private static void translateOreVein(CTNHLangProvider provider, String key, String cn) {
        provider.addOreVein(key, cn);
    }
    public static void nuclearTranslation(Material material, String name) {
        var nuclearProperty = material.getProperty(CTNHPropertyKeys.NUCLEAR);
        translateMaterial(provider, material, name);
        translateMaterial(provider, nuclearProperty.getCarbideMaterial(), "碳化" + name);
        translateMaterial(provider, nuclearProperty.getOxideMaterial(), "氧化" + name);
        translateMaterial(provider, nuclearProperty.getNitrideMaterial(), "氮化" + name);
        translateMaterial(provider, nuclearProperty.getZirconiumAlloyMaterial(), "锆合金" + name);
        translateMaterial(provider, GTMaterials.get(material.getName() + "_hexafluoride"), "六氟化" + name);
        translateMaterial(provider, GTMaterials.get(material.getName() + "_hexafluoride_steam_cracked"), "蒸汽裂解的六氟化" + name);
    }
    public static void nuclearTranslation2(Material material, String name) {
        var nuclearProperty = material.getProperty(CTNHPropertyKeys.NUCLEAR);
        translateMaterial(provider, nuclearProperty.getCarbideMaterial(), "碳化" + name);
        translateMaterial(provider, nuclearProperty.getOxideMaterial(), "氧化" + name);
        translateMaterial(provider, nuclearProperty.getNitrideMaterial(), "氮化" + name);
        translateMaterial(provider, nuclearProperty.getZirconiumAlloyMaterial(), "锆合金" + name);
        if(material.equals(GTMaterials.Plutonium239)){
            translateMaterial(provider, GTMaterials.get(material.getName() + "_239_hexafluoride"), "六氟化" + name);
            translateMaterial(provider, GTMaterials.get(material.getName() + "_239_hexafluoride_steam_cracked"), "蒸汽裂解的六氟化" + name);
        }
        else {
            translateMaterial(provider, GTMaterials.get(material.getName() + "_hexafluoride"), "六氟化" + name);
            translateMaterial(provider, GTMaterials.get(material.getName() + "_hexafluoride_steam_cracked"), "蒸汽裂解的六氟化" + name);
        }
    }
